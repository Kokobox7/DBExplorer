CREATE USER reader WITH PASSWORD 'reader';
GRANT SELECT ON ALL TABLES IN SCHEMA public  TO "reader";
GRANT SELECT ON ALL TABLES IN SCHEMA bookings  TO "reader";
GRANT USAGE ON SCHEMA bookings  TO "reader";

CREATE USER admin WITH PASSWORD 'admin';
GRANT SELECT, INSERT, DELETE ON ALL TABLES IN SCHEMA public  TO "admin";
GRANT USAGE ON ALL SEQUENCES IN SCHEMA public  TO "admin";
GRANT SELECT, INSERT, DELETE ON ALL TABLES IN SCHEMA bookings  TO "admin";
GRANT USAGE ON SCHEMA bookings  TO "admin";